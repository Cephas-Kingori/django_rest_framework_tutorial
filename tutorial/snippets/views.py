@api_view(['GET', 'POST'])
def snippet2_list(request, format=None):
    """
    List all code snippets, or create a new snippet.
    """
    # get will return all the serialized objects in the database

    if request.method == 'GET':
        snippets = Snippet.objects.all()
        serializer = BetterSnippetSerializer(snippets, many=True)
        return Response(serializer.data)

# POST is usually used to create a new resouce
    elif request.method == 'POST':
        serializer = BetterSnippetSerializer(data=request.data)
        if serializer.is_valid():
            serializer.save()
            return Response(serializer.data, status=status.HTTP_201_CREATED)
        return Response(serializer.errors, status=status.HTTP_400_BAD_REQUEST)


